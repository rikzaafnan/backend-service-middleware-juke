package api_public_service.entity;

public class Tag {
    private long id;
    private String name;

    // Getter dan Setter
    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
